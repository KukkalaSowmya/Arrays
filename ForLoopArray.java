public class ForLoopArray{
  public static void main(String[] args) {
    //create an array 
    int[] age = {12,3,5,7,13,8,9,2};
    //loop through the array
    //using for loop
    System.out.println("Using for loop: ");
    for(int i = 0; i<age.length; i++) {
      System.out.println(age[i]);
    } 
  }
}